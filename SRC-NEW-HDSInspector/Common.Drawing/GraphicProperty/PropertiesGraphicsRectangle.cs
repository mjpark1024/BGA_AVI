        public PropertiesGraphicsRectangle(GraphicsRectangle rectangle)
        {
            if (rectangle == null)
            {
                throw new ArgumentNullException("rectangle");
            }

            this.left = rectangle.Left;
            this.top = rectangle.Top;
            this.right = rectangle.Right;
            this.bottom = rectangle.Bottom;
            this.lineWidth = rectangle.LineWidth;
            this.regionType = rectangle.RegionType;
            this.objectColor = rectangle.ObjectColor;
            this.actualScale = rectangle.ActualScale;
            this.Id = rectangle.ID;
            this.selected = rectangle.IsSelected;
            this.symmetryValue = rectangle.SymmetryValue;
            this.iterationValue = rectangle.IterationValue;
            this.iterationXPosition = rectangle.IterationXPosition;
            this.iterationYPosition = rectangle.IterationYPosition;
            this.blockIterationValue = rectangle.BlockIterationValue;
            this.isFiducialRegion = rectangle.IsFiducialRegion;
            this.isValidRegion = rectangle.IsValidRegion;
            this.motherROI = rectangle.MotherROI;
            this.caption = rectangle.Caption;

            if (rectangle.MotherROI != null)
            {
                this.motherROIID = rectangle.MotherROI.ID; // şążř˝Ă Mother ROI·ÎŔÇ ĂßŔűĽşŔ» Č®ş¸ÇĎ±â Ŕ§ÇŘ ÇĘżäÇĎ´Ů.
            }

            if (rectangle.InspectionList != null)
            {
                inspectionItems = new InspectionItem[rectangle.InspectionList.Count];
                int nIndex = 0;
                foreach (InspectionItem inspectionElement in rectangle.InspectionList)
                {
                    inspectionItems[nIndex++] = inspectionElement;
                }
            }
        }
        #endregion

        public override GraphicsBase CreateGraphics()
        {
            lineWidth = 2; // Default thickness.

            List<InspectionItem> inspectionList = new List<InspectionItem>();
            foreach (InspectionItem inspectionElement in inspectionItems)
            {
                inspectionList.Add(InspectionItemHelper.CopyInspectionItem(inspectionElement));
            }

            actualScale = (actualScale > 0) ? actualScale : 1.0;
            GraphicsRectangle b = new GraphicsRectangle(left, top, right, bottom, lineWidth, regionType, objectColor,
                                                   actualScale, symmetryValue, iterationValue, blockIterationValue,
                                                   isFiducialRegion, motherROI,
                                                   caption, inspectionList,
                                                   iterationXPosition, iterationYPosition);
            b.IsValidRegion = isValidRegion;
            b.MotherROIID = motherROIID;

            // GraphicsBase contains new ID generated by constructor.
            // This is OK if this instance is loaded from XML file.
            // But if this instance is part of commands history,
            // it contains ID which should be set in GraphicsBase.
            // This rule is applied to all SerializedGraphicsBase-derived
            // classes.
            if (this.Id != 0)       // from command history, not from file
            {
                b.ID = this.Id;
                b.IsSelected = this.selected;
            }

            return b;
        }

